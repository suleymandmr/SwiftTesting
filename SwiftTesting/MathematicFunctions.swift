class MathematicFunctions {
    
    func addInteger(x: Int, y: Int)-> Int{
        return x + y
    }
    
    func MultiplyNumbers(x: Int, y: Int)-> Int{
        return x * y
    }
    
    func DivideInteger(x: Int, y: Int)-> Int{
        return x / y
    }
    
